# Make sure a VBS launcher exists so the scheduled task can run PowerShell
# completely hidden (no console window flashes up on every interval).
function Ensure-VbsLauncher {
    $vbs = @'
' WinCopy-Hidden.vbs - launches WinCopy-Run.ps1 without showing any window.
' Generated by WinCopy-GUI.ps1; safe to delete (will be re-created).
Option Explicit
Dim shell, scriptDir, ps1, cmd
Set shell  = CreateObject("WScript.Shell")
scriptDir  = CreateObject("Scripting.FileSystemObject").GetParentFolderName(WScript.ScriptFullName)
ps1        = scriptDir & "\WinCopy-Run.ps1"
cmd        = "powershell.exe -NoProfile -NonInteractive -WindowStyle Hidden -ExecutionPolicy Bypass -File """ & ps1 & """"
' Run hidden (0), do not wait.
shell.Run cmd, 0, False
'@
    Set-Content -Path $VbsLauncher -Value $vbs -Encoding ASCII
}

